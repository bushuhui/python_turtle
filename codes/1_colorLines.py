import turtle

def draw():
    turtle.bgcolor("black")
    turtle.speed(4)

    p = turtle.Pen()

    p.pencolor("red")
    p.forward(100)
    p.pencolor("orange")
    p.forward(100)
    p.pencolor("yellow")
    p.forward(100)

    p.right(90)
    p.pencolor("blue")
    p.forward(94)
    p.pencolor("indigo")
    p.forward(94)
    p.pencolor("violet")
    p.forward(94)

    p.right(90)
    p.pencolor("red")
    p.forward(102)
    p.pencolor("orange")
    p.forward(102)
    p.pencolor("yellow")
    p.forward(102)
    p.pencolor("blue")
    p.forward(102)
    p.pencolor("indigo")
    p.forward(102)
    p.pencolor("violet")
    p.forward(102)

    p.right(90)
    p.pencolor("red")
    p.forward(96)
    p.pencolor("orange")
    p.forward(96)
    p.pencolor("yellow")
    p.forward(96)
    p.pencolor("blue")
    p.forward(96)
    p.pencolor("indigo")
    p.forward(96)
    p.pencolor("violet")
    p.forward(96)

    p.right(90)
    p.pencolor("red")
    p.forward(102)
    p.pencolor("orange")
    p.forward(102)
    p.pencolor("yellow")
    p.forward(102)
    p.pencolor("blue")
    p.forward(102)
    p.pencolor("indigo")
    p.forward(102)
    p.pencolor("violet")
    p.forward(102)

    p.right(90)
    p.pencolor("red")
    p.forward(100)
    p.pencolor("orange")
    p.forward(100)
    p.pencolor("yellow")
    p.forward(100)

if __name__ == "__main__":
    draw()
    turtle.mainloop()
